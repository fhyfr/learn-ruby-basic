# the difference between puts and print is
# puts automatically prints a new line after the end of a sentence
# print will prints the sentence in single line


# example of puts
first_name, last_name = "Firman", "Sah"

# the output is
# Firman
# Sah
puts first_name
puts last_name

# example of print
# the output is
# FirmanSah
print first_name
print last_name
